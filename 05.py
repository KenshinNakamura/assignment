#05 四則演算
#整数値を2つ入力させ、それらの値の和、差、積、商と余りを求めるプログラムを作成せよ。
#なお、差と商は1つ目の値から2つ目の値を引いた、あるいは割った結果とする。
#余りは無い場合も0と表示するのでよい。
input  = 123
input2 = 7
print(input + input2)
print(input - input2)
print(input * input2)
print(input // input2)
print(input % input2)

